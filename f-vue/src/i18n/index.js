Object.assign(errorMessages, extraErrorMessages);

function normalizeErrorSource(value) {
  return String(value).replace(/\s+/g, '').replace(/[，。、“”「」『』（）()：:]/g, '');
}

function localizedMessages(code, base) {
  return mergeMessages(base, localeOverrides[code] || {});
}

const messages = {
  'zh-CN': zhCN,
  en,
  'zh-TW': localizedMessages('zh-TW', zhTW),
  fa: localizedMessages('fa', fa),
  vi: localizedMessages('vi', vi),
  ru: localizedMessages('ru', ru),
};

export const locale = ref(
  typeof localStorage !== 'undefined' ? localStorage.getItem(LOCALE_KEY) || 'zh-CN' : 'zh-CN',
);

function lookup(key, lang) {
  const parts = String(key).split('.');
  let cur = messages[lang];
  for (const part of parts) {
    cur = cur?.[part];
    if (cur === undefined) return undefined;
  }
  return cur;
}

export function t(key, params) {
  void locale.value;
  let text = lookup(key, locale.value) ?? lookup(key, 'zh-CN') ?? lookup(key, 'en') ?? key;
  if (params && typeof text === 'string') {
    for (const [k, v] of Object.entries(params)) {
      text = text.replaceAll(`{${k}}`, String(v));
    }
  }
  return text;
}

export function i18nError(message, fallbackKey = 'common.failed') {
  const raw = String(message || '').trim();
  const lang = locale.value;
  if (!raw) return t(fallbackKey);

  const exact = errorMessages[raw]?.[lang];
  if (exact) return exact;

  for (const [source, translations] of Object.entries(errorMessages)) {
    if (raw.includes(source) && translations[lang]) {
      return raw.replace(source, translations[lang]);
    }
    if (normalizeErrorSource(raw).includes(normalizeErrorSource(source)) && translations[lang]) {
      return translations[lang];
    }
  }

  return raw;
}

export function setLocale(code) {
  if (!messages[code]) return;
  locale.value = code;
  if (typeof localStorage !== 'undefined') {
    localStorage.setItem(LOCALE_KEY, code);
  }
  if (typeof document !== 'undefined') {
    document.documentElement.lang = code;
    document.documentElement.dir = code === 'fa' ? 'rtl' : 'ltr';
  }
}

export function useI18n() {
  return { locale, t, setLocale, languages: SUPPORTED_LOCALES };
}

if (typeof document !== 'undefined') {
  setLocale(locale.value);
}
